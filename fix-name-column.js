require('dotenv').config({ path: '.env.local' })

const postgres = require('postgres')

const sql = postgres(process.env.DATABASE_URL, {
  ssl: 'require'
})

async function fixNameColumn() {
  try {
    console.log('üîß Correction de la colonne name...')
    
    // Rendre la colonne name nullable
    await sql`
      ALTER TABLE users ALTER COLUMN name DROP NOT NULL;
    `
    
    console.log('‚úÖ Colonne name rendue nullable!')
    
    // V√©rifier les contraintes
    const constraints = await sql`
      SELECT column_name, is_nullable 
      FROM information_schema.columns 
      WHERE table_name = 'users'
      ORDER BY ordinal_position
    `
    
    console.log('üìã Contraintes des colonnes:')
    constraints.forEach(col => {
      console.log(`  - ${col.column_name}: nullable=${col.is_nullable}`)
    })
    
  } catch (error) {
    console.error('‚ùå Erreur:', error)
  } finally {
    await sql.end()
  }
}

fixNameColumn()
