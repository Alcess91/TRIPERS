require('dotenv').config({ path: '.env.local' })

const postgres = require('postgres')

const sql = postgres(process.env.DATABASE_URL, {
  ssl: 'require'
})

async function checkAndMigrate() {
  try {
    console.log('üîç V√©rification de la structure de la table users...')
    
    // V√©rifier la structure actuelle
    const columns = await sql`
      SELECT column_name, data_type, is_nullable 
      FROM information_schema.columns 
      WHERE table_name = 'users'
      ORDER BY ordinal_position
    `
    
    console.log('üìã Colonnes actuelles:')
    columns.forEach(col => {
      console.log(`  - ${col.column_name}: ${col.data_type} (${col.is_nullable})`)
    })
    
    // V√©rifier si password_hash existe
    const hasPasswordHash = columns.some(col => col.column_name === 'password_hash')
    
    if (!hasPasswordHash) {
      console.log('‚ùå Colonne password_hash manquante!')
      console.log('üîß Ex√©cution de la migration...')
      
      // Lire le fichier de migration
      const fs = require('fs')
      const migrationSQL = fs.readFileSync('./database/migration.sql', 'utf8')
      
      // Ex√©cuter la migration
      await sql.unsafe(migrationSQL)
      console.log('‚úÖ Migration ex√©cut√©e avec succ√®s!')
      
      // V√©rifier √† nouveau
      const newColumns = await sql`
        SELECT column_name, data_type 
        FROM information_schema.columns 
        WHERE table_name = 'users'
        ORDER BY ordinal_position
      `
      
      console.log('üìã Nouvelles colonnes:')
      newColumns.forEach(col => {
        console.log(`  - ${col.column_name}: ${col.data_type}`)
      })
    } else {
      console.log('‚úÖ La table users a la bonne structure!')
    }
    
  } catch (error) {
    console.error('‚ùå Erreur:', error)
  } finally {
    await sql.end()
  }
}

checkAndMigrate()
